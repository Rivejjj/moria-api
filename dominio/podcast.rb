class Podcast
  def initialize(info_podcast)
    @info_podcast = info_podcast
  end

  def nombre
    @info_podcast.nombre
  end

  def autor
    @info_podcast.autor
  end

  def anio
    @info_podcast.anio
  end

  def duracion
    @info_podcast.duracion
  end

  def genero
    @info_podcast.genero
  end
end
